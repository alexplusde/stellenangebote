MForm-Formular, folgende Abfragen / Parameter:

1. YForm-Select: Ein oder mehrere Stellenangebote-YForm-Kategorie - wenn leer, alle ausgeben

2. Select: Reihenfolge der Angebote: Neue zuerst oder wichtige zuerst (Standard: wichtige zuerst)

3. Select: Pro Kategorie ausgeben oder alle gemeinsam?

4. Checkbox: Suchfunktion aktivieren
5. Checkbox: Filter nach Stadt aktivieren
6. Checkbox: Filter nach Kategorie aktivieren